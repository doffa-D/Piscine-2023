char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] = str[i] + 32;
		else if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
